package recap_demo_classes;

public class FourTransactions {
	public int sum(int num1, int num2) {
		return num1 + num2;
	}

	public int div(int num1, int num2) {
		return num1 / num2;
	}

	public int multiply(int num1, int num2) {
		return num1 * num2;
	}

	public int subraction(int num1, int num2) {
		return num1 - num2;
	}
}
